using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace ReportesAppBorrarClase
{
    class ReporteVisual
    {
        int idReporte;
        String numero;
        String nombreEquipo;
        String problemaPosibleText;
        DateTime fechaHora;
        String nombreCliente;
        String estado;
        String nombrePC;

        public int IdReporte { get { return idReporte; } set{ idReporte = value; } }
        public String Numero { get { return numero; } set { numero = value; } }
        public String NombreEquipo { get { return nombreEquipo; } set { nombreEquipo = value; } }
        public String ProblemaPosibleText { get { return problemaPosibleText; } set { problemaPosibleText = value; } }
        public DateTime FechaHora { get { return fechaHora; } set { fechaHora = value; } }
        public String NombreCliente { get { return nombreCliente; } set { nombreCliente = value; } }
        public String Estado { get { return estado; } set { estado = value; } }
        public String NombrePC { get { return nombrePC; } set { nombrePC = value; } }
        
       
        public ReporteVisual(int idReporte, String numero, String nombreEquipo, String problemaPosibleText, 
                             DateTime fechaHora, String nombreCliente, String estado, String nombrePC)
        {
            Numero = numero;
            NombreEquipo = nombreEquipo;
            ProblemaPosibleText = problemaPosibleText;
            FechaHora = fechaHora;
            NombreCliente = nombreEquipo;
            Estado = estado;
            NombrePC = nombrePC;
        }
    }
}
